// If

let edad = 18;

if (edad == 18){
    console.log("Tu ya eres Legal");
}else{
    console.log("eres menor de edad");
}


// switch

let edad = 5;

switch(edad){
    case 17:
        console.log("Eres Menor de edad")
        break;

    case 18:
        console.log("Eres Legal")
        break;
    
    default:
        console.log("no eres humano")
        
}

